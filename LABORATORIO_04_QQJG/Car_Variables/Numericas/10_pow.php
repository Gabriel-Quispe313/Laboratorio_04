<?php
// Definimos la base y el exponente
$base = 2;
$exponente = 3;

// Calculamos la potencia usando pow()
$resultado = pow($base, $exponente);

// Mostramos el resultado
echo "$base elevado a la potencia de $exponente es: $resultado\n"; 

// También podemos calcular otra potencia
$base2 = 5;
$exponente2 = 4;
$resultado2 = pow($base2, $exponente2);

// Mostramos el resultado de la segunda potencia
echo "$base2 elevado a la potencia de $exponente2 es: $resultado2\n"; 
?>