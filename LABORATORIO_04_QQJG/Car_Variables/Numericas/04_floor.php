<?php
// Definimos algunos números decimales
$numero1 = 4.7;
$numero2 = 9.3;
$numero3 = -2.8;

// Usamos floor() para redondear los números hacia abajo
$redondeado1 = floor($numero1);
$redondeado2 = floor($numero2);
$redondeado3 = floor($numero3);

// Mostramos los resultados
echo "Número original: $numero1, Redondeado: $redondeado1\n"; 
echo "Número original: $numero2, Redondeado: $redondeado2\n"; 
echo "Número original: $numero3, Redondeado: $redondeado3\n"; 
?>