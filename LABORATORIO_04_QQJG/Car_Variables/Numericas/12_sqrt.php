<?php
// Definimos un número
$numero = 16;

// Calculamos la raíz cuadrada usando sqrt()
$raiz_cuadrada = sqrt($numero);

// Mostramos el resultado
echo "La raíz cuadrada de $numero es: $raiz_cuadrada\n"; 

// También podemos calcular la raíz cuadrada de otro número
$numero2 = 25;
$raiz_cuadrada2 = sqrt($numero2);

// Mostramos el resultado de la segunda raíz cuadrada
echo "La raíz cuadrada de $numero2 es: $raiz_cuadrada2\n"; 
?>